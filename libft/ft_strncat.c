#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		j;
	size_t	k;

	i = ft_strlen((const char *)s1);
	j = -1;
	k = 0;
	while (s2[++j] && k++ < n)
		s1[i++] = s2[j];
	s1[i] = '\0';
	return (s1);
}
