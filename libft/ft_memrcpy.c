#include "libft.h"

void	*ft_memrcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	while (src && n != 0)
	{
		n--;
		d[n] = s[n];
	}
	return (dst);
}
