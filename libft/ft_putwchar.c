#include "libft.h"

void	ft_putwchar(wchar_t wc)
{
	char	s[4];
	int		len;

	ft_bzero(s, 4);
	len = ft_wctomb(s, wc);
	if (len < 1)
		return ;
	ft_putstr(s);
}
