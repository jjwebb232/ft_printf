#include "libft.h"

int	ft_wordcount(char const *s, char d)
{
	int len;

	len = 0;
	while (*s)
	{
		if (*s == d)
			s++;
		if (*s && *s != d)
		{
			len++;
			s += ft_wordlen(s, d);
		}
	}
	return (len);
}
