#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int s;
	int t;

	i = 0;
	s = 1;
	t = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		s = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && (ISNUM(str[i])))
	{
		t *= 10;
		t += str[i++] - '0';
	}
	return (s * t);
}
